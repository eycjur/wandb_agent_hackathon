function buildConfig(
  domain: DomainId,
  rubric: string[],
  targetTemplate: string,
  judgeTemplate: string,
  passThreshold: number = 4
): DomainPromptConfig {
  const rubricBullets = rubric.map((item) => `- ${item}`).join("\n");
  const targetInstruction = targetTemplate.replace("{{RUBRIC_BULLETS}}", rubricBullets).trim();
  const judgeInstruction = judgeTemplate.replace("{{RUBRIC_BULLETS}}", rubricBullets).trim();

  return {
    domain,
    rubricVersion: 1,
    passThreshold,
    targetInstruction,
    judgeInstruction,
    judgeRubric: rubric,
    samples: SAMPLES
  };
}

